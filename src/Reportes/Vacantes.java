    private void nombreuoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nombreuoActionPerformed
        try{
             String nombuo=((String)nombreuo.getSelectedItem());
             List<Cargo> cargos = CapitalHumano.getVacancy(nombuo);
             
             DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
             tableModel.setRowCount(0);
             for (Cargo element : cargos) {
                
                int vacancy = vacancy(element.getPlazasA(), element.getPlazasC()); 
                
                Object[] row = {element.getNombre(),element.getGrupoEscala(), element.getSalario(), element.getCalificacion(), vacancy};
                tableModel.addRow(row);
             }
        }catch (SQLException ex) {
            Logger.getLogger(CapitalHumano.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_nombreuoActionPerformed

